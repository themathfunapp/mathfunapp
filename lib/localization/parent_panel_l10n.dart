class ParentPanelL10n {
  ParentPanelL10n._();

  static String of(String? languageCode, String key) {
    final code = (languageCode ?? 'en').toLowerCase();
    final map = switch (code) {
      'tr' => _tr,
      'de' => _de,
      'es' => _es,
      'fr' => _fr,
      'ar' => _ar,
      'fa' => _fa,
      'zh' => _zh,
      'id' => _id,
      'ku' => _ku,
      'ru' => _ru,
      'ja' => _ja,
      'ko' => _ko,
      'hi' => _hi,
      'ur' => _ur,
      'pt' => _pt,
      'it' => _it,
      'pl' => _pl,
      _ => _en,
    };
    return map[key] ?? _en[key] ?? key;
  }
}

